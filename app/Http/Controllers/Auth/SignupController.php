    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required|string|max:191',
            'surname' => 'required|string|max:191',
            'username' => 'required|string|max:191',
            'email' => 'required|string|email|max:191|unique:users',
            'password' => 'required|string|confirmed|min:5|max:50',
        ]);

        $user = User::create([
            'name' => $request->name,
            'surname' => $request->surname,
            'username' => $request->username,
            'email' => $request->email,
            'type' => $request->account_type,
            'password' => Hash::make($request->password),
            'show_username' => $request->show_username ? 1 : 0,
            'receive_news' => $request->receive_news ? 1 : 0,
        ]);

        $signup_state = $user->save();

        Auth::login($user);
        
        Session::flash('success', 'Created your account successfully.');

        return redirect(RouteServiceProvider::HOME);
    }

